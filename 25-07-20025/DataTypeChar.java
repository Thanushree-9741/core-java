class DataTypeChar {
    public static void main(String[] args) {
        char[] letters = {'A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J'};
        System.out.println(letters.length);
        for (int i = 0; i < letters.length; i++) {
            System.out.println(letters[i]);
        }
    }
}
