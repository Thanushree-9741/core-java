package Package.inheritance;

public class LaptopInherit {


        public final void brand() {
            System.out.println("Laptop: Brand is Dell");
        }


        public static void type() {
            System.out.println("Laptop: General purpose");
        }


        private void password() {
            System.out.println("Laptop: Default BIOS password");
        }


        protected void performance() {
            System.out.println("Laptop: Normal performance");
        }


        void price() {
            System.out.println("Laptop: Price is 50,000 INR");
        }
    }

